# --- 3-Calcule a subtração entre os dois numeros através do metodo
# --- 4-Calcule a multiplicação entre os dois numeros através do metodo
# --- 5-Calcule a divisão inteira entre os dois numeros através do metodo
# --- 6-Calcule a divisão fracionada entre os dois numeros através do metodo
# --- 7-Calcule o resto da divisão entre os dois numeros através do metodo
# --- 8-Calcule a raiz entre os dois numeros através do metodo
# --- 9-Separa os metodos em outro arquivo



def soma(n1,n2):
    return n1+n2

def subtracao(n1,n2): 
    return n1-n2

def multiplicacao(n1,n2):
    return n1*n2

def divisao_inteira(n1,n2):
    return  n1//n2

def divisao_fracionada(n1,n2):
    return n1/n2

def resto_divisao(n1,n2):
    return n1%n2

def raiz(n1,n2):
    return n1**(1/n2)



